import { CreateDateColumn, DeleteDateColumn, UpdateDateColumn } from 'typeorm';

export class EntityBase {
  @DeleteDateColumn({ type: 'timestamp', default: null })
  deletedAt: Date;

  @CreateDateColumn({
    type: 'timestamp',
    default: () => 'CURRENT_TIMESTAMP(6)',
  })
  createdAt: Date;

  @UpdateDateColumn({
    type: 'timestamp',
    default: () => 'CURRENT_TIMESTAMP(6)',
    onUpdate: 'CURRENT_TIMESTAMP(6)',
  })
  updatedAt: Date;
}
